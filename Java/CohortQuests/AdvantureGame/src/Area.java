public class Area {
    private String areaName;
    private Monster monster;
    private String reward;
    private boolean areaCleared;

    public String getAreaName() {
        return areaName;
    }

    public Monster getMonster() {
        return monster;
    }

    public String getReward() {
        return reward;
    }

    public boolean isAreaCleared() {
        return areaCleared;
    }
}
